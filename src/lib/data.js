export function getAreaNote(areaId, key) {
  const custom = AREA_NOTES[areaId]?.[key];
  if (custom) return custom;
  const area = AREAS.find(a => a.id === areaId);
  if (!area) return ["詳細データはPhase2で追加予定です。"];
  const axNames = {walk:"散歩・公園環境",housing:"住環境・家賃",medical:"医療・サポート施設",mobility:"移動・カーシェア",community:"地域コミュニティ"};
  const top = Object.entries({walk:area.walk,housing:area.housing,medical:area.medical,mobility:area.mobility,community:area.community}).sort((a,b)=>b[1]-a[1]);
  const lines = [
    `${axNames[top[0][0]]}が区内上位水準（スコア${top[0][1]}）`,
    `${axNames[top[1][0]]}も強み（スコア${top[1][1]}）`,
    `${axNames[top[top.length-1][0]]}はやや低め（スコア${top[top.length-1][1]}）、事前確認を`,
  ];
  const dog = key?.[1];
  if(dog==="X") lines.push("小型犬OKの物件・カフェを優先的に探せるエリア");
  else if(dog==="Y") lines.push("大型犬の運動量を補える散歩コースへのアクセスを確認して");
  else if(dog==="Z") lines.push("シニア犬のかかりつけ医は早めに確保しておくのがおすすめ");
  return lines;
}
