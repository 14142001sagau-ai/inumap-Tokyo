export function calcScores(areas, w) {
  const tot = Object.values(w).reduce((a,b)=>a+b, 0);
  const raws = areas.map(d => AXES.reduce((s,ax)=>s+(d[ax.k]||0)*w[ax.k],0)/tot);
  const avg = raws.reduce((a,b)=>a+b,0)/raws.length;
  const sd = Math.sqrt(raws.reduce((a,b)=>a+(b-avg)**2,0)/raws.length)||1;
  return areas.map((d,i)=>({...d, dev: Math.round((raws[i]-avg)/sd*10+50)}));
}

